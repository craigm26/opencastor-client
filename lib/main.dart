import 'dart:async';
import 'package:firebase_core/firebase_core.dart';
import 'package:flutter/foundation.dart' show kIsWeb;
import 'package:flutter/material.dart';
import 'package:flutter_riverpod/flutter_riverpod.dart';

import 'app.dart';
import 'core/sw_updater.dart';
import 'data/services/auth_service.dart';
import 'firebase_options.dart'; // generated by flutterfire configure — real values injected by CI

// Captures any startup error so we can surface it as a visible screen
// instead of a black screen. Reset to null once app loads successfully.
String? _startupError;

Future<void> main() async {
  WidgetsFlutterBinding.ensureInitialized();

  // On web: trigger a service-worker update check on every app start.
  // Without this, stale SWs persist across Cloudflare Pages deploys and
  // serve wrong cached resources — including blocking Firestore requests.
  // Fire-and-forget: never blocks app startup. No-op on native platforms.
  if (kIsWeb) triggerSwUpdate();

  // Global Flutter framework error handler — catches widget tree errors
  // that survive the ProviderScope and would otherwise show a black screen.
  FlutterError.onError = (FlutterErrorDetails details) {
    FlutterError.presentError(details);
    debugPrint('FlutterError: ${details.exception}\n${details.stack}');
  };

  try {
    await Firebase.initializeApp(options: DefaultFirebaseOptions.currentPlatform);
    // Complete any pending redirect sign-in (web only).
    // Guarded by a 5s timeout — getRedirectResult() can hang if the Firebase
    // auth domain is slow or unreachable, which would block runApp() forever.
    await AuthService.handleRedirectResult()
        .timeout(const Duration(seconds: 5), onTimeout: () {});
  } catch (e, st) {
    // Firebase init failure must never black-screen the app.
    // Store the error so _ErrorApp can display it on screen for debugging.
    _startupError = '$e\n\n$st';
    debugPrint('Firebase init error (running offline): $e');
  }

  runApp(
    _startupError != null
        ? _ErrorApp(error: _startupError!)
        : const ProviderScope(child: OpenCastorApp()),
  );
}

/// Shown when Firebase (or another startup step) throws before runApp().
/// Displays the full error on-screen so TestFlight/Simulator testers can
/// read it without needing Xcode console access.
class _ErrorApp extends StatelessWidget {
  const _ErrorApp({required this.error});
  final String error;

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      debugShowCheckedModeBanner: false,
      home: Scaffold(
        backgroundColor: const Color(0xFF0e1416),
        body: SafeArea(
          child: Padding(
            padding: const EdgeInsets.all(20),
            child: Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                const Text(
                  '⚠️  Startup Error',
                  style: TextStyle(
                    color: Color(0xFFffba38),
                    fontSize: 20,
                    fontWeight: FontWeight.bold,
                  ),
                ),
                const SizedBox(height: 8),
                const Text(
                  'Firebase failed to initialize. '
                  'App cannot start. Error details:',
                  style: TextStyle(color: Colors.white70, fontSize: 14),
                ),
                const SizedBox(height: 16),
                Expanded(
                  child: SingleChildScrollView(
                    child: SelectableText(
                      error,
                      style: const TextStyle(
                        color: Color(0xFF55d7ed),
                        fontSize: 12,
                        fontFamily: 'monospace',
                      ),
                    ),
                  ),
                ),
              ],
            ),
          ),
        ),
      ),
    );
  }
}
